namespace Backend_ML_Gesture_Common.Models
{
    public class Gyroscope
    {
        private float x;

        public float X
        {
            get { return x; }
            set
            {
                x = value;
            }
        }

        private float y;

        public float Y
        {
            get { return y; }
            set
            {
                y = value;
            }
        }

        private float z;

        public float Z
        {
            get { return z; }
            set
            {
                z = value;
            }
        }
    }
}